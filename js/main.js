let currentCharacter = null;
let currentSections = [];
let activeDialog = null;
let currentAudioLabel = "Musik";
let activeBackgroundLayer = pageBackgroundPrimary;
let inactiveBackgroundLayer = pageBackgroundSecondary;

function hasOpenDialog() {
  return [legalOverlay, galleryOverlay, audioConsent].some(
    (overlay) => !overlay.classList.contains("hidden")
  );
}

function getOpenDialog() {
  return [audioConsent, galleryOverlay, legalOverlay].find(
    (overlay) => !overlay.classList.contains("hidden")
  ) || null;
}

function updateAudioUi() {
  const isPlaying = !audio.paused;
  audioToggle.textContent = isPlaying ? `\u23F8 ${currentAudioLabel}` : `\u25B6 ${currentAudioLabel}`;
  audioToggle.setAttribute("aria-pressed", String(isPlaying));
  audioStatus.textContent = isPlaying ? "Musik spielt." : "Musik ist pausiert.";
}

async function playAudio() {
  try {
    await audio.play();
  } catch (_error) {
    updateAudioUi();
  }
}

function getFocusableElements(container) {
  return Array.from(
    container.querySelectorAll(
      'button, [href], input, select, textarea, [tabindex]:not([tabindex="-1"])'
    )
  ).filter((element) => !element.hasAttribute("disabled"));
}

function openDialog(dialog, trigger) {
  activeDialog = dialog;
  dialog.dataset.returnFocus = trigger?.id || "";
  dialog.classList.remove("hidden");
  dialog.setAttribute("aria-hidden", "false");
  body.classList.add("modal-open");
  getFocusableElements(dialog)[0]?.focus();
}

function closeDialog(dialog) {
  if (dialog.classList.contains("hidden")) {
    return;
  }

  dialog.classList.add("hidden");
  dialog.setAttribute("aria-hidden", "true");
  activeDialog = getOpenDialog();

  if (dialog === legalOverlay) {
    openLegalButton.setAttribute("aria-expanded", "false");
  }

  if (dialog === galleryOverlay) {
    jumpToGalleryButton.setAttribute("aria-expanded", "false");
  }

  if (dialog === audioConsent && !siteShell.classList.contains("hidden")) {
    audioToggle.focus();
  }

  body.classList.toggle("modal-open", hasOpenDialog());

  const returnFocusId = dialog.dataset.returnFocus;

  if (returnFocusId && dialog !== audioConsent) {
    document.getElementById(returnFocusId)?.focus();
  }
}

function populateSections(config) {
  currentSections = config.sections;

  menuItems.forEach((item, index) => {
    const section = currentSections[index];
    item.dataset.target = section.id;
    item.setAttribute("aria-controls", section.id);
    item.textContent = section.nav;
  });

  contentArticles.forEach((article, index) => {
    const section = currentSections[index];
    const heading = article.querySelector("h2");
    const contentBody = article.querySelector(".content-body");

    article.id = section.id;
    heading.textContent = section.title;
    contentBody.replaceChildren(
      ...section.paragraphs.map((paragraph) => {
        const paragraphElement = document.createElement("p");
        paragraphElement.textContent = paragraph;
        return paragraphElement;
      })
    );
  });
}

function setSectionBackground(imagePath) {
  if (!imagePath) {
    activeBackgroundLayer.classList.remove("is-active");
    activeBackgroundLayer.style.backgroundImage = "";
    inactiveBackgroundLayer.classList.remove("is-active");
    inactiveBackgroundLayer.style.backgroundImage = "";
    return;
  }

  if (activeBackgroundLayer.style.backgroundImage.includes(imagePath)) {
    return;
  }

  inactiveBackgroundLayer.style.backgroundImage = `linear-gradient(180deg, rgba(4, 6, 10, 0.28), rgba(4, 6, 10, 0.28)), url("${imagePath}")`;
  inactiveBackgroundLayer.classList.add("is-active");
  activeBackgroundLayer.classList.remove("is-active");

  const previousActive = activeBackgroundLayer;
  activeBackgroundLayer = inactiveBackgroundLayer;
  inactiveBackgroundLayer = previousActive;
  inactiveBackgroundLayer.style.backgroundImage = "";
}

function setActiveSection(sectionId) {
  const nextSection =
    currentSections.find((section) => section.id === sectionId) || currentSections[0];

  if (!nextSection) {
    return;
  }

  menuItems.forEach((item) => {
    const isActive = item.dataset.target === nextSection.id;
    item.classList.toggle("active", isActive);
    item.setAttribute("aria-pressed", String(isActive));
  });

  contentArticles.forEach((article) => {
    const isActive = article.id === nextSection.id;
    article.classList.toggle("active", isActive);
    article.hidden = !isActive;
  });

  setSectionBackground(nextSection.background);
}

function renderGallery(_config) {
  galleryGrid.replaceChildren();
  const emptyState = document.createElement("p");
  emptyState.className = "gallery-empty";
  emptyState.textContent = "Weitere Eindrücke folgen.";
  galleryGrid.append(emptyState);
}

function applyCharacter(characterKey) {
  const config = CHARACTER_CONFIG[characterKey];

  currentCharacter = characterKey;
  body.dataset.character = characterKey;
  characterEyebrow.textContent = config.eyebrow;
  characterTitle.textContent = config.title;
  characterTitleLine.textContent = config.titleLine;
  characterSubtitle.textContent = config.subtitle;
  currentAudioLabel = config.audioLabel;

  populateSections(config);
  renderGallery(config);
  audio.pause();
  audioSource.src = config.audioSource;
  audio.load();
  updateAudioUi();

  selectionScreen.classList.add("hidden");
  siteShell.classList.remove("hidden");
  setActiveSection("prolog");
  openDialog(audioConsent, audioToggle);
}

function openSelectionScreen() {
  closeDialog(legalOverlay);
  closeDialog(galleryOverlay);
  closeDialog(audioConsent);
  currentCharacter = null;
  body.dataset.character = "none";
  audio.pause();
  updateAudioUi();
  setSectionBackground("");
  siteShell.classList.add("hidden");
  selectionScreen.classList.remove("hidden");
  selectionButtons[0]?.focus();
}

function trapFocus(event) {
  if (!activeDialog || event.key !== "Tab") {
    return;
  }

  const focusableElements = getFocusableElements(activeDialog);

  if (focusableElements.length === 0) {
    event.preventDefault();
    return;
  }

  const first = focusableElements[0];
  const last = focusableElements[focusableElements.length - 1];

  if (event.shiftKey && document.activeElement === first) {
    event.preventDefault();
    last.focus();
  } else if (!event.shiftKey && document.activeElement === last) {
    event.preventDefault();
    first.focus();
  }
}

